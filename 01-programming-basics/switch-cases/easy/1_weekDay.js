function weekDay(day) {
  switch (day) {
    case 1:
      return "Saturday";
    case 2:
      return "Sunday";
    case 3:
      return "Monday";
    case 4:
      return "Tuesday";
    case 5:
      return "Wednsday";
    case 6: 
       return "Thursday"; 
    case 7:
        return "Friday";
    default:
        return "Invalid day";
  }
}
